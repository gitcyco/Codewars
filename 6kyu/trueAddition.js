// 6 kyu True Addition
//
// Given a target number (n), return that number.
// 
// You are not allowed to use any numbers (or string representations of numbers).
// 
// You are not allowed to declare any variables other than the one given (x).
// 
// You are not allowed to set variable x to equal n, either directly or by using 'arguments'.
// 
// You are not allowed to use 'arguments' to reference n.
// 
// You may not use 'Array'.
// 
// You may not return variable n. Your function must return variable x.
//
// Answer:
function count(n){
    var x = true;
    while(++x !== n) continue;
    return x;
}